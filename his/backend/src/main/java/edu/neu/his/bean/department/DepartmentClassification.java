package edu.neu.his.bean.department;

/**
 * 该类代表科室分类
 *
 * @author 王婧怡
 * @version 1.0
 */
public class DepartmentClassification {
    private int id;
    private String name;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
